        ​​<br>​<br>
        The Guwahati theatre festival aims to showcase world-class performances with quality entertainment, working
        towards becoming a prestigious landmark in the cultural landscape of Guwahati.
        ​​<br>​<br>
        The idea of the festival is to bring spectrum of plays from all over india to allow audiences in Guwahati to get
        a feel of how production works and what kind of innovation are taking place in the outside theatres which are
        not from assam. The one-of-a-kind event aims to provide a platform in the region for diverse range of world
        class performing groups, local as well as national, to showcase their performances amongst new audiences
        infusing freshness and vivacity. The annual festival, now in its second year, visions to create a dynamic
        platform bringing new and dynamic voices & performances to the stage.
        ​​<br>​<br>
        The festival has fulfilled the avid appetite of the city to experience and understand the relevance of theatre;
        in context of our human relationships, culture and stereotypes.

